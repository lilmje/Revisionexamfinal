#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;
	int num;

	i = 0;
	if (ac < 1)
		write(1, "\n", 1);
	if (ac > 1)
	{
		while (av[1][i] != '\0')
		{
			if (av[1][i] >= 'a' &&  av[1][i] <= 'z')
			{
				num = av[1][i]  - 'a' + 1;
				j = 0;
				while(j < num)
				{
					write(1, &av[1][i], 1);
					j++;
				}
			}
			else
				write(1, &av[1][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
