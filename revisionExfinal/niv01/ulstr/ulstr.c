#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;
	char *num;

	i = 1;
	j = 0;
	if (ac < 2)
		write(1, "\n", 1);
	if (ac > 2)
		write(1, "\n", 1);
	if (ac == 2)
	{
		while (av[1][j] != '\0')
		{
			if(av[1][j] >= 'a' && av[1][j] <= 'z')
			{
				av[1][j] -= 32;
				write(1, &av[1][j], 1);
			}
			else if(av[1][j] >= 'A' && av[1][j] <= 'Z')
			{
				av[1][j] += 32;
				write(1, &av[1][j], 1);
			}
			else
				write(1, &av[1][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
}
